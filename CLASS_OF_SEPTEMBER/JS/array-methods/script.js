var cars = ["Camery","Corolla","GLK","Venza","Lexus350"];
console.log(cars);

// pop - removes element from the end of an array
cars.pop();
console.log(cars)


// push - adds element to the end of an array
cars.push('Highlander')
console.log(cars);


// unshift - adds element to the beginning of an array
cars.unshift('Pathfinder');
console.log(cars);

// shift - removes element from the beginning of an array
cars.shift();
console.log(cars);

// sort
cars.sort();
console.log(cars)


